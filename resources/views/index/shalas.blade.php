Pakashala

Sanskrit Word Pakashala (पाकशाला) in Sanskrit is composed of two terms 'Paka' and 'Sala' where 'Paka' means 'cooking' and 
'sala' means 'a place or a house'. So, Pakashala means 'a house of cooking' i.e a kitchen (रसोईघर).

Pakashala in Jagatguru Mahayogi Siddhababa Academy provides free meals to everyone regardless of caste, race, and religion and is the one and only "खुला अन्न क्षेत्र" of the region where free meals are served to anyone at any time visiting the academy. Meals are fully prepared and served by the sadhus of the academy in the Vaishnav pattern. In Vaishnavism, pure sattvic vegetarian meals are cooked and offered to ista devata ( god) and only then served.

The source for meals in pakshala is fully donation based.

Every day, Academy serves free meals to more than 500 devotee on average. 

